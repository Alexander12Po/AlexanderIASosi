# Main Gemini AI tool using direct HTTP requests
class GeminiAITool:
    def __init__(self, api_key: str):
        self.api_key = api_key
        self.history = []
        self.model_name = None
        self.base_url = "https://generativelanguage.googleapis.com/v1beta"
        
    def list_models(self):
        """List available models to find the correct one"""
        url = f"{self.base_url}/models?key={self.api_key}"
        try:
            response = requests.get(url)
            if response.status_code == 200:
                models = response.json().get('models', [])
                # Find a suitable gemini model
                for model in models:
                    if 'gemini' in model['name'].lower() and 'generateContent' in model.get('supportedGenerationMethods', []):
                        self.model_name = model['name'].split('/')[-1]  # Extract model name
                        print(f"[INFO] Using model: {self.model_name}")
                        return True
                # Fallback to first gemini model
                for model in models:
                    if 'gemini' in model['name'].lower():
                        self.model_name = model['name'].split('/')[-1]
                        print(f"[INFO] Using model: {self.model_name}")
                        return True
                print("[ERROR] No suitable Gemini model found")
                return False
            else:
                print(f"[ERROR] Failed to list models: {response.status_code}")
                return False
        except Exception as e:
            print(f"[ERROR] Exception while listing models: {str(e)}")
            return False
    
    def send_prompt(self, prompt: str) -> str:
        if not self.model_name:
            if not self.list_models():
                return "[ERROR] Could not determine model to use"
        
        url = f"{self.base_url}/models/{self.model_name}:generateContent?key={self.api_key}"
        
        headers = {
            "Content-Type": "application/json"
        }
        
        data = {
            "contents": [{
                "parts": [{
                    "text": prompt
                }]
            }]
        }
        
        try:
            response = requests.post(url, headers=headers, json=data)
            if response.status_code == 200:
                result = response.json()
                # Extract the response text correctly
                ai_response = result['candidates'][0]['content']['parts'][0]['text']
                self.history.append({"prompt": prompt, "response": ai_response})
                return ai_response
            else:
                return f"[ERROR] API Error: {response.status_code} - {response.text}"
        except Exception as e:
            return f"[ERROR] Failed to generate response: {str(e)}"
    
    def show_history(self):
        if not self.history:
            print("[INFO] No conversation history")
            return
        
        print("\n[+] Conversation History:")
        for i, entry in enumerate(self.history, 1):
            print(f"\n--- Prompt {i} ---")
            print(f"Q: {entry['prompt']}")
            print(f"A: {entry['response'][:200]}...")  # Truncate for display
            
    def clear_history(self):
        self.history = []
        print("[INFO] Conversation history cleared")

# Encoder/Decoder section (as requested)
def encode_string(text: str) -> str:
    """Simple encoding function to make it difficult to decode"""
    encoded = ""
    for char in text:
        encoded += str(ord(char) * 3 + 7) + " "
    return encoded.strip()

def decode_string(encoded: str) -> str:
    """Decoder function"""
    try:
        numbers = encoded.split(" ")
        decoded = ""
        for num in numbers:
            if num:
                decoded += chr(int((int(num) - 7) / 3))
        return decoded
    except:
        return "[ERROR] Decoding failed"

